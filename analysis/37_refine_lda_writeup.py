from docx import Document

SRC = "/Users/dxk/Downloads/段晓坤 毕业论文二稿_LDA补强版.docx"
OUT = "/Users/dxk/Downloads/段晓坤 毕业论文二稿_LDA优化版.docx"

doc = Document(SRC)

for p in doc.paragraphs:
    t = p.text.strip()
    if t.startswith("为进一步验证帖子文本的主题聚合方向"):
        p.text = (
            "为进一步验证帖子文本的主题聚合方向，本文另行采用 LDA 对帖子标题与正文进行了补充性主题提取，"
            "并对不同主题数方案进行比较。在 K=3 至 K=7 的试验中，K=4 的主题划分在关键词可解释性上相对更稳定，"
            "因此本文将其作为补充性结果加以呈现。需要说明的是，LDA结果不作为本文的主分析依据，"
            "而主要用于辅助判断高频词与共现网络所呈现的内容结构是否具有较稳定的主题归纳基础。"
        )
    elif t.startswith("作为补充性检验，LDA 主题提取将帖子文本大体归纳为五类内容主题"):
        p.text = (
            "作为补充性检验，LDA 主题提取将帖子文本大体归纳为四类内容主题："
            "一是以“人生、时间、工作、朋友”等词为代表的情绪成长与工作叙事主题；"
            "二是以“宝宝、营养、奶粉、妈妈”等词为代表的母婴营养与喂养场景主题；"
            "三是以“春天、温柔、拍照、春日”等词为代表的春日氛围与日常审美表达主题；"
            "四是以“皮肤、抗老、精华、护肤”等词为代表的护肤抗老与成分种草主题。"
            "这一补充性结果与前文基于高频词和共现网络得到的判断总体一致，说明高影响力生活类达人并非围绕单一赛道展开表达，"
            "而是在泛生活叙事中嵌入若干相对稳定的垂直主题。"
        )

doc.save(OUT)
print(OUT)
